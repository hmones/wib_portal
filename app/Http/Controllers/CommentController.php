     * @return \Illuminate\Http\Response
     */
    public function index(Post $post)
    {
        $comments = $post->comments()->latest()->paginate(3);
        
        if($comments->count() > 0){
            return view('partials.comments.list',compact('comments'));
        }

        return response('Error',200);
        
    }

